package com.example.lab5;

public class StrokePoint {
    private final float x;
    private final float y;

    public StrokePoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
